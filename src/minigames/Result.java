    private void tableUpdate(LinkedList hasilTestList){
        
        DefaultTableModel d = (DefaultTableModel)tableList.getModel(); // get table`
        
        d.setRowCount(0); 
        
        // set data in table
        int i = 1;
        int countBenar = 0;
        int countSalah = 0;
        for (Iterator it = hasilTestList.iterator(); it.hasNext();) {
            HasilTest ht = (HasilTest) it.next();
            //  ht.tampilkanData(); //-- untuk test cek
            
            Vector v2 = new Vector();
            v2.add(i++);
            v2.add(ht.qst);
            v2.add(ht.ans);
            v2.add(ht.result);
            v2.add(ht.tf);
            if(ht.tf){
                countBenar++;
            }else{
                countSalah++;
                
            }
            d.addRow(v2);
        }    
        
        // set qty benar
            qtyBenar.setText("Benar : "+Integer.toString(countBenar));
        
        // set qty salah
            qtySalah.setText("Salah : "+Integer.toString(countSalah));
            
        // nilai
            System.out.println("benar : "+countBenar);
            System.out.println("salah : "+countSalah);
            float nilaiAkhir = (float) countBenar /(countBenar+countSalah) * 100;
            System.out.println("akhir : "+nilaiAkhir);
            nilai.setText("NILAI : "+Float.toString(nilaiAkhir));
        
    }
    
       
